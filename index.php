            $('.service_list_content').each(function(i) {
                if ($(this).position().top + bannerhgt <= scroll) {
                    $('.service_list_sticky_image .service_image.active').removeClass('active');
                    $('.service_list_sticky_image .service_image').eq(i).addClass('active');
                }
            });



            if (scroll <= topFixedSpace) {
                $(".service_list_sticky_image").removeClass("position_fixed");
